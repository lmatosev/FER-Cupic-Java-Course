/**
 * 
 * Type of the token.
 *
 */

public enum TokenType {
	/**
	 * Type indicating the end of input.
	 */
	EOF,
	/**
	 * Type indicating a word.
	 */
	WORD,
	/**
	 * Type indicating a number.
	 */
	NUMBER,
	/**
	 * Type indicating a simbol.
	 */
	SYMBOL
}
